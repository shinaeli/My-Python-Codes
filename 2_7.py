user_name = ' Fredrick\t Lord\n Lugard '

print(user_name)
# Fredrick	 Lord
# Lugard

user_name2 = ' Derrick Rose '

# 'lstrip()' removes the spacing on the left hand side of the string
print(user_name2.lstrip())
# 'rstrip()' removes the spacing on the right hand side of the string
print(user_name2.rstrip())
# 'rstrip()' removes the spacing on both side of the string
print(user_name2.strip())
