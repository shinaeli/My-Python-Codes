# Write a program that asks the user for a weight in kilograms and converts it to pounds. There
# are 2.2 pounds in a kilogram.

user_weight = int(input('Provide your weight in kilogram: '))
converted_weight = user_weight * 2.2
print(f'Your weight is {converted_weight}lbs')

